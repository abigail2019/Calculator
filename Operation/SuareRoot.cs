using System;
using System.Collections.Generic;
using System.Text;

namespace Operation
{
   public class SuareRoot
    {
        public static double Root(int a)
        {
            return Math.Pow(a, 1.0 / 2.0);
        }

        public static double Root(double a)
        {
            return Math.Pow(a, 1.0 / 2.0);
        }

    }
}
